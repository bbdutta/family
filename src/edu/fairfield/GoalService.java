package edu.fairfield;

public class GoalService {

	private Integer goalServiceId;
	private Integer goalId;
	private String goalName;
	private Integer servicelId;
	private String serviceName;

	
	public Integer getGoalServiceId() {
		return goalServiceId;
	}

	public void setGoalServiceId(Integer goalServiceId) {
		this.goalServiceId = goalServiceId;
	}

	public Integer getGoalId() {
		return goalId;
	}

	public void setGoalId(Integer goalId) {
		this.goalId = goalId;
	}

	public String getGoalName() {
		return goalName;
	}

	public void setGoalName(String goalName) {
		this.goalName = goalName;
	}

	public Integer getServicelId() {
		return servicelId;
	}

	public void setServicelId(Integer servicelId) {
		this.servicelId = servicelId;
	}

	public String getServiceName() {
		return serviceName;
	}

	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}

}
